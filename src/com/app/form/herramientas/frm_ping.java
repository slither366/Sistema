    private class PingThread extends Thread {

        private final JTextArea texto;

        public PingThread(JTextArea texto) {
            this.texto = texto;
            this.start();
        }

        @Override
        public void run() {
            String url = txtIP.getText();
            this.texto.setText("Haciendo ping a " + url + ":");
            int total = 0, correcto = 0, error = 0;
            boolean p;
            try {
                Thread.sleep(500);
            } catch (InterruptedException e) {
            }
            while (total < 4) {
                total++;
                try {
                    URL host = new URL("http://" + url);
                    HttpURLConnection http = (HttpURLConnection) host.openConnection();
                    http.connect();
                    this.texto.setText(this.texto.getText() + "\n" + "Respuesta desde " + url + ":");
                    correcto++;
                    p = true;
                } catch (IOException e) {
                    this.texto.setText(this.texto.getText() + "\n" + "Tiempo de espera agotado para esta solicitud.");
                    error++;
                    p = false;
                }
                try {
                    if (p) {
                        Thread.sleep(1000);
                    }
                } catch (InterruptedException e) {
                }
            }
            try {
                Thread.sleep(500);
            } catch (InterruptedException e) {
            }
            this.texto.setText(this.texto.getText() + "\n" + "Estad√≠sticas de ping para " + url + ":");
            this.texto.setText(this.texto.getText() + "\n" + "Paquetes: Enviados: " + total + ", recibidos: " + correcto + ", perdidos: " + error);
            int valor;
            try {
                valor = correcto * 100 / error;
            } catch (ArithmeticException ex) {
                valor = 0;
            }
            this.texto.setText(this.texto.getText() + "\n" + "(" + valor + "% perdidos)");
        }
    }
