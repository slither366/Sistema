    private com.app.paleta.txtNumeros txtFila;
    private com.app.paleta.txtTexto txtNombre_Producto;
    private com.app.paleta.txtNumeros txtPrecioV_Actual;
    private com.app.paleta.txtNumeros txtPrecioV_Inicial;
    private com.app.paleta.txtNumeros txtStockMinimo;
