    private void txtDescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDescuentoActionPerformed
        if (this.txtDescuento.getNumeroInt() < 0) {
            MensajeSistema.MensajeVarios(this, "El Descuento no puede ser menor a Cero...", MensajeSistema.INFORMATION_MESSAGE());
            this.txtDescuento.setNumero(this.txtFactor.getNumeroInt() / 2);
            this.txtDescuento.grabFocus();
        } else {
            if (this.txtDescuento.getNumeroInt() > (this.txtFactor.getNumeroInt() / 2)) {
                MensajeSistema.MensajeVarios(this, "El Descuento no puede ser mayor a la mitad del Factor!!!", MensajeSistema.ERROR_MESSAGE());
                this.txtDescuento.setNumero(this.txtFactor.getNumeroInt() / 2);
                this.txtDescuento.grabFocus();
            } else {
                if (this.txtDescuento.getText().isEmpty()) {
                    this.txtDescuento.setNumero(0);
                }
                this.txtStockMinimo.grabFocus();
            }
        }
    }//GEN-LAST:event_txtDescuentoActionPerformed

