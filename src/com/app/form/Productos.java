    private void txtCod_BarraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCod_BarraActionPerformed
        int valor = this.txtCod_Barra.verificarVacioConMsj();
        if (valor == 0) {
            //Cuando es Agregar se verifica si el codigo de barra ya existe en la base de datos
            if (operacion == 'A') {
                String resu = this.getConexion.getDescripcion(tablaConsutada, "Bar_Codigo",
                        new String[]{cod_empresa, "Bar_Codigo"},
                        new String[]{Configuracion.getCOD_EMPRESA(), this.txtCod_Barra.getText().trim()});
                if (resu != null) {
                    MensajeSistema.MensajeVarios(this, "Este Código de producto ya existe en la Base de Datos...", MensajeSistema.ERROR_MESSAGE());
                    this.txtCod_Barra.grabFocus();
                } else {
                    this.txtCod_Seccion.grabFocus();
                }
            } else {
                if (this.recuperado) {
                    this.txtCod_Seccion.grabFocus();
                } else {
                    this.txtCod_Producto.setEnabled(false);
                    this.txtNombre_Producto.setEnabled(false);
                    vTipo = 2;
                    this.RecuperarDatos(this.txtCod_Barra.getText());
                }
            }
        } else if (valor == 1) {
            this.Inicializar();
        }
    }//GEN-LAST:event_txtCod_BarraActionPerformed

