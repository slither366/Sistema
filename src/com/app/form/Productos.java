    private void txtEstanteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtEstanteKeyPressed
        if (evt.getKeyCode() == ClaseTeclas.VK_ENTER()) {
            if (this.txtEstante.getNumeroInt() < 0) {
                MensajeSistema.MensajeVarios(this, "El valor de este campo no puede ser menor a Cero...", MensajeSistema.ERROR_MESSAGE());
                this.txtEstante.setNumero(0);
            } else {
                if (this.txtEstante.getNumeroInt() == 0) {
                    this.txtComentario.setEnabled(true);
                    this.btnGrabar.setEnabled(true);
                    this.txtColumna.setEnabled(false);
                    this.txtFila.setEnabled(false);
                    this.txtEstante.setNumero(0);
                    this.txtColumna.setNumero(0);
                    this.txtFila.setNumero(0);
                    this.txtComentario.grabFocus();
                } else {
                    this.txtColumna.grabFocus();
                }
            }
        }
    }//GEN-LAST:event_txtEstanteKeyPressed

