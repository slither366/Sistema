    @Override
    public void Inicializar() {
        ClaseCampos.setAllNullComponents(jPanelDatos);
        ClaseCampos.setEnabled(jPanelDatos, false);
        this.vTipo = 0;
        this.recuperado = false;
        this.jTab.setSelectedIndex(0);
        this.jTab.setEnabledAt(1, false);
        this.txtComentario.setEnabled(false);
        this.txtComentario.setText("");
        this.textNom_Envace.setText("");
        this.textNom_Linea.setText("");
        this.textNom_Marca.setText("");
        this.textNom_Seccion.setText("");
        this.textNom_Procedencia.setText("");
        this.btnNuevo.setEnabled(Agrega_OK);
        this.btnModificar.setEnabled(Modifica_OK);
        this.btnBorrar.setEnabled(Borra_OK);
        this.btnGrabar.setEnabled(false);
        this.btnCancelar.setEnabled(false);
        this.btnSalir.setEnabled(true);
        if (this.btnNuevo.isEnabled()) {
            this.btnNuevo.grabFocus();
        } else if (this.btnModificar.isEnabled()) {
            this.btnModificar.grabFocus();
        } else if (this.btnBorrar.isEnabled()) {
            this.btnBorrar.grabFocus();
        } else {
            this.btnSalir.grabFocus();
        }
    }

    @Override
    public void Agregar() {
        this.jTab.setSelectedIndex(0);
        this.jTab.setEnabledAt(1, false);
        if (getConexion.autoNumerico(tablaConsutada, idConsultada,
                new String[]{cod_empresa}, new String[]{Configuracion.getCOD_EMPRESA()}, this.txtCod_Producto)) {
            ClaseBotones.modoEdicionABM(btnNuevo, btnModificar, btnBorrar, btnGrabar, btnCancelar, btnSalir, false);
            ClaseCampos.setEnabled(jPanelDatos, true);
            this.txtComentario.setEnabled(true);
            this.txtCosto_Actual.setEnabled(false);
            this.txtPrecioV_Actual.setEnabled(false);
            this.btnGrabar.setEnabled(false);
            this.txtNombre_Producto.setEnabled(true);
            operacion = 'A';
            this.cargarCombos();
            this.txtCod_Producto.setEnabled(false);
            this.txtNombre_Producto.grabFocus();
        } else {
            Inicializar();
        }
    }

    @Override
    public void Editar(char c) {
        operacion = c;
        ClaseBotones.modoEdicionABM(btnNuevo, btnModificar, btnBorrar, btnGrabar, btnCancelar, btnSalir, false);
        this.txtCod_Producto.setEnabled(true);
        this.txtCod_Barra.setEnabled(true);
        this.btnGrabar.setEnabled(false);
        this.cargarCombos();
        this.txtCod_Producto.grabFocus();
    }

    @Override
    public void Grabar() {
        if (operacion == 'A') {
            if (MensajeSistema.Guardar(this)) {
                if (getConexion.insertar(tablaConsutada,
                        new String[]{cod_empresa, idConsultada, "Bar_Codigo", descripcionConsultada,
                            txtCod_Linea.getBdCodigo(), txtCod_Procedencia.getBdCodigo(), txtCod_Envace.getBdCodigo(),
                            txtCod_Marca.getBdCodigo(), txtCod_Seccion.getBdCodigo(), "peresedero", "controlado", "ubi_estante", "ubi_columna",
                            "ubi_fila", "TIva_Codigo", "TProd_Codigo", "factor", "descuento", "costo_inicial",
                            "venta_inicial", "costo_actual", "venta_actual", "comentario", "stock_minimo", "Usu_Codigo"},
                        new String[]{Configuracion.getCOD_EMPRESA(), this.txtCod_Producto.getText(), this.txtCod_Barra.getText(),
                            this.txtNombre_Producto.getText(), this.txtCod_Linea.getText(), this.txtCod_Procedencia.getText(),
                            this.txtCod_Envace.getText(), this.txtCod_Marca.getText(), this.txtCod_Seccion.getText(),
                            this.jCheckPedesedero.isSelected() ? "1" : "2", this.jCheckControlado.isSelected() ? "1" : "2", this.txtEstante.getNumeroInt() + "",
                            this.txtColumna.getNumeroInt() + "", this.txtFila.getNumeroInt() + "", this.jComboIva.getSelectedIndex() + "",
                            this.jComboTipoProducto.getSelectedIndex() + "", this.txtFactor.getNumeroInt() + "",
                            this.txtDescuento.getNumeroInt() + "", this.txtCosto_Inicial.getNumeroInt() + "", this.txtPrecioV_Inicial.getNumeroInt() + "",
                            this.txtCosto_Actual.getNumeroInt() + "", this.txtPrecioV_Actual.getNumeroInt() + "",
                            this.txtComentario.getText().trim(), this.txtStockMinimo.getNumeroInt() + "", Configuracion.getCOD_USUARIO()})) {
                }
            }
        } else if (operacion == 'M') {
            if (MensajeSistema.Modificar(this)) {
                getConexion.actualizar(tablaConsutada,
                        new String[]{"Bar_Codigo", descripcionConsultada, txtCod_Linea.getBdCodigo(), txtCod_Procedencia.getBdCodigo(),
                            txtCod_Envace.getBdCodigo(), txtCod_Marca.getBdCodigo(), txtCod_Seccion.getBdCodigo(), "peresedero", "controlado", "ubi_estante",
                            "ubi_columna", "ubi_fila", "TIva_Codigo", "TProd_Codigo", "factor", "descuento", "costo_actual", "venta_actual",
                            "comentario", "stock_minimo", "Usu_Codigo"},
                        new String[]{this.txtCod_Barra.getText(), this.txtNombre_Producto.getText(), this.txtCod_Linea.getText(),
                            this.txtCod_Procedencia.getText(), this.txtCod_Envace.getText(), this.txtCod_Marca.getText(), this.txtCod_Seccion.getText(),
                            this.jCheckPedesedero.isSelected() ? "1" : "2", this.jCheckControlado.isSelected() ? "1" : "2",
                            this.txtEstante.getNumeroInt() + "", this.txtColumna.getNumeroInt() + "", this.txtFila.getNumeroInt() + "",
                            this.jComboIva.getSelectedIndex() + "", this.jComboTipoProducto.getSelectedIndex() + "", this.txtFactor.getNumeroInt() + "",
                            this.txtDescuento.getNumeroInt() + "", this.txtCosto_Actual.getNumeroInt() + "", this.txtPrecioV_Actual.getNumeroInt() + "",
                            this.txtComentario.getText().trim(), this.txtStockMinimo.getNumeroInt() + "", Configuracion.getCOD_USUARIO()},
                        new String[]{this.cod_empresa, idConsultada},
                        new String[]{Configuracion.getCOD_EMPRESA(), this.txtCod_Producto.getText()});
            }
        }
        Inicializar();
    }

    @Override
    public void RecuperarDatos(String codigo) {
        String[] resultado = getConexion.getDescripciones("vst_" + tablaConsutada,
                new String[]{idConsultada, "Bar_Codigo", descripcionConsultada, txtCod_Linea.getBdCodigo(), txtCod_Linea.getBdDescrip(),
                    txtCod_Procedencia.getBdCodigo(), txtCod_Procedencia.getBdDescrip(), txtCod_Envace.getBdCodigo(), txtCod_Envace.getBdDescrip(),
                    txtCod_Marca.getBdCodigo(), txtCod_Marca.getBdDescrip(), txtCod_Seccion.getBdCodigo(), txtCod_Seccion.getBdDescrip(),
                    "peresedero", "controlado", "ubi_estante", "ubi_columna", "ubi_fila", "TIva_Codigo", "TProd_Codigo", "factor", "descuento",
                    "costo_inicial", "venta_inicial", "costo_actual", "venta_actual", "comentario", "stock_minimo"},
                new String[]{this.cod_empresa, vTipo == 1 ? this.txtCod_Producto.getBdCodigo() : "Bar_Codigo"},
                new String[]{Configuracion.getCOD_EMPRESA(), codigo});
        if (resultado[0] != null) {
            this.txtCod_Producto.setText(resultado[0]);
            this.txtCod_Barra.setText(resultado[1]);
            this.txtNombre_Producto.setText(resultado[2]);
            this.txtCod_Linea.setText(resultado[3]);
            this.textNom_Linea.setText(resultado[4]);
            this.txtCod_Procedencia.setText(resultado[5]);
            this.textNom_Procedencia.setText(resultado[6]);
            this.txtCod_Envace.setText(resultado[7]);
            this.textNom_Envace.setText(resultado[8]);
            this.txtCod_Marca.setText(resultado[9]);
            this.textNom_Marca.setText(resultado[10]);
            this.txtCod_Seccion.setText(resultado[11]);
            this.textNom_Seccion.setText(resultado[12]);
            this.jCheckPedesedero.setSelected("1".equals(resultado[13]));
            this.jCheckControlado.setSelected("1".equals(resultado[14]));
            this.txtEstante.setText(resultado[15]);
            this.txtColumna.setText(resultado[16]);
            this.txtFila.setText(resultado[17]);
            this.jComboIva.setSelectedIndex(Integer.parseInt(resultado[18]));
            this.jComboTipoProducto.setSelectedIndex(Integer.parseInt(resultado[19]));
            this.txtFactor.setText(resultado[20]);
            this.txtDescuento.setText(resultado[21]);
            this.txtCosto_Inicial.setNumero(resultado[22]);
            this.txtPrecioV_Inicial.setNumero(resultado[23]);
            this.txtCosto_Actual.setNumero(resultado[24]);
            this.txtPrecioV_Actual.setNumero(resultado[25]);
            this.txtComentario.setText(resultado[26]);
            this.txtStockMinimo.setText(resultado[27]);
            if (operacion == 'E') {
                Borrar(this, tablaConsutada,
                        new String[]{cod_empresa, idConsultada},
                        new String[]{Configuracion.getCOD_EMPRESA(), codigo});
                Inicializar();
            } else if (operacion == 'M') {
                this.recuperado = true;
                ClaseCampos.setEnabled(jPanelDatos, true);
                this.txtCosto_Inicial.setEnabled(false);
                this.txtPrecioV_Inicial.setEnabled(false);
                this.txtCod_Producto.setEnabled(false);
                this.txtCod_Barra.setEnabled(false);
                this.txtComentario.setEnabled(true);
                this.txtNombre_Producto.grabFocus();
            }
        } else {
            if (MensajeSistema.ConsultaSQLVacio(this)) {
                this.recuperado = false;
                if (vTipo == 1) {
                    this.txtCod_Producto.setText("");
                    this.txtCod_Producto.setEnabled(true);
                    this.txtCod_Producto.grabFocus();
                } else {
                    this.txtCod_Barra.setText("");
                    this.txtCod_Barra.setEnabled(true);
                    this.txtCod_Barra.grabFocus();
                }
            } else {
                this.Inicializar();
            }
        }
    }

    private void cargarCombos() {
        this.jComboTipoProducto.removeAllItems();
        this.jComboTipoProducto.addItem("Seleccione...");
        this.jComboTipoProducto.addItem("MERCADERIA");
        this.jComboTipoProducto.addItem("SERVICIOS");
        this.jComboTipoProducto.addItem("ACTIVO FIJO");
        this.jComboTipoProducto.addItem("CONTRATO");
        this.jComboTipoProducto.addItem("GASTOS");
        this.jComboIva.removeAllItems();
        this.jComboIva.addItem("Seleccione...");
        this.jComboIva.addItem("EXENTA");
        this.jComboIva.addItem("IVA 05 %");
        this.jComboIva.addItem("IVA 10 %");
    }
