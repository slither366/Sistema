    @Override
    public void RecuperarDatos(String codigo) {
        String[] resultado = getConexion.getDescripciones("vst_" + this.txtCod_Producto.getBdTabla(),
                new String[]{this.txtCod_Producto.getBdCodigo(), "Bar_Codigo", this.txtCod_Producto.getBdDescrip(), txtCod_Linea.getBdCodigo(), txtCod_Linea.getBdDescrip(),
                    txtCod_Procedencia.getBdCodigo(), txtCod_Procedencia.getBdDescrip(), txtCod_Envace.getBdCodigo(), txtCod_Envace.getBdDescrip(),
                    txtCod_Marca.getBdCodigo(), txtCod_Marca.getBdDescrip(), txtCod_Seccion.getBdCodigo(), txtCod_Seccion.getBdDescrip(),
                    "peresedero", "controlado", "ubi_estante", "ubi_columna", "ubi_fila", "TIva_Codigo", "TProd_Codigo", "factor", "descuento",
                    "costo_inicial", "venta_inicial", "costo_actual", "venta_actual", "comentario", "stock_minimo"},
                new String[]{this.EMP_CODIGO, vTipo == 1 ? this.txtCod_Producto.getBdCodigo() : "Bar_Codigo"},
                new String[]{Configuracion.getEMP_CODIGO(), codigo});
        if (resultado[0] != null) {
            this.txtCod_Producto.setText(resultado[0]);
            this.txtCod_Barra.setText(resultado[1]);
            this.txtNombre_Producto.setText(resultado[2]);
            this.txtCod_Linea.setText(resultado[3]);
            this.textNom_Linea.setText(resultado[4]);
            this.txtCod_Procedencia.setText(resultado[5]);
            this.textNom_Procedencia.setText(resultado[6]);
            this.txtCod_Envace.setText(resultado[7]);
            this.textNom_Envace.setText(resultado[8]);
            this.txtCod_Marca.setText(resultado[9]);
            this.textNom_Marca.setText(resultado[10]);
            this.txtCod_Seccion.setText(resultado[11]);
            this.textNom_Seccion.setText(resultado[12]);
            this.jCheckPedesedero.setSelected("1".equals(resultado[13]));
            this.jCheckControlado.setSelected("1".equals(resultado[14]));
            this.txtEstante.setText(resultado[15]);
            this.txtColumna.setText(resultado[16]);
            this.txtFila.setText(resultado[17]);
            this.cboIva.setSelectCodigo(resultado[18]);
            this.cboTipoProducto.setSelectCodigo(resultado[19]);
            this.txtFactor.setText(resultado[20]);
            this.txtDescuento.setText(resultado[21]);
            this.txtCosto_Inicial.setNumero(resultado[22]);
            this.txtPrecioV_Inicial.setNumero(resultado[23]);
            this.txtCosto_Actual.setNumero(resultado[24]);
            this.txtPrecioV_Actual.setNumero(resultado[25]);
            this.txtComentario.setText(resultado[26]);
            this.txtStockMinimo.setText(resultado[27]);
            if (Operacion == 'E') {
                this.txtCod_Producto.Borrar();
                Inicializar();
            } else {
                this.recuperado = true;
                ClaseCampos.setEnabled(jPanelDatos, true);
                this.txtCosto_Inicial.setEnabled(false);
                this.txtPrecioV_Inicial.setEnabled(false);
                this.txtCod_Producto.setEnabled(false);
                this.txtCod_Barra.setEnabled(false);
                this.txtComentario.setEnabled(true);
                this.txtNombre_Producto.grabFocus();
            }
        } else {
            if (MensajeSistema.ConsultaSQLVacio(this)) {
                this.recuperado = false;
                if (vTipo == 1) {
                    this.txtCod_Producto.setText("");
                    this.txtCod_Producto.setEnabled(true);
                    this.txtCod_Producto.grabFocus();
                } else {
                    this.txtCod_Barra.setText("");
                    this.txtCod_Barra.setEnabled(true);
                    this.txtCod_Barra.grabFocus();
                }
            } else {
                this.Inicializar();
            }
        }
    }
