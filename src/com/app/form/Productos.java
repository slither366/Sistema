    private void txtCosto_InicialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCosto_InicialActionPerformed
        if (this.txtCosto_Inicial.getNumeroInt() < 0) {
            MensajeSistema.MensajeVarios(this, "El Precio de Costo no puede ser menor a Cero", MensajeSistema.INFORMATION_MESSAGE());
            this.txtCosto_Inicial.setText("");
            this.txtCosto_Inicial.grabFocus();
        } else {
            this.txtCosto_Actual.setNumero(this.txtCosto_Inicial.getNumeroInt());
            double vVenta = ((this.txtCosto_Inicial.getNumeroInt() * this.txtFactor.getNumeroInt()) / 100) + this.txtCosto_Inicial.getNumeroInt();
            this.txtPrecioV_Inicial.setNumero(vVenta);
            this.txtPrecioV_Inicial.grabFocus();
        }
    }//GEN-LAST:event_txtCosto_InicialActionPerformed

    private void txtPrecioV_InicialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPrecioV_InicialActionPerformed
        if (this.txtPrecioV_Inicial.getNumeroInt() < this.txtCosto_Inicial.getNumeroInt()) {
            MensajeSistema.MensajeVarios(this, "El Precio de Venta no puede ser menor a Costo", MensajeSistema.INFORMATION_MESSAGE());
            this.txtPrecioV_Inicial.setNumero(((this.txtCosto_Inicial.getNumeroInt() * this.txtFactor.getNumeroInt()) / 100) + this.txtCosto_Inicial.getNumeroInt());
            this.txtPrecioV_Inicial.grabFocus();
        } else {
            this.txtPrecioV_Actual.setNumero(this.txtPrecioV_Inicial.getNumeroInt());
            this.jCheckPedesedero.grabFocus();
        }
    }//GEN-LAST:event_txtPrecioV_InicialActionPerformed

    private void txtDescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDescuentoActionPerformed
        if (this.txtDescuento.getNumeroInt() < 0) {
            MensajeSistema.MensajeVarios(this, "El Descuento no puede ser menor a Cero...", MensajeSistema.INFORMATION_MESSAGE());
            this.txtDescuento.setNumero(this.txtFactor.getNumeroInt() / 2);
            this.txtDescuento.grabFocus();
        } else {
            if (this.txtDescuento.getNumeroInt() > (this.txtFactor.getNumeroInt() / 2)) {
                MensajeSistema.MensajeVarios(this, "El Descuento no puede ser mayor a la mitad del Factor!!!", MensajeSistema.ERROR_MESSAGE());
                this.txtDescuento.setNumero(this.txtFactor.getNumeroInt() / 2);
                this.txtDescuento.grabFocus();
            } else {
                if (this.txtDescuento.getText().isEmpty()) {
                    this.txtDescuento.setNumero(0);
                }
                this.txtStockMinimo.grabFocus();
            }
        }
    }//GEN-LAST:event_txtDescuentoActionPerformed

