    @Override
    public void Inicializar() {
        ClaseCampos.setTextNull(jPanelDatos);
        ClaseCampos.setEnabled(jPanelDatos, false);
        this.vTipo = 0;
        this.recuperado = false;
        this.jTab.setSelectedIndex(0);
        this.jTab.setEnabledAt(1, false);
        this.txtComentario.setEnabled(false);
        this.txtComentario.setText("");
        this.textNom_Envace.setText("");
        this.textNom_Linea.setText("");
        this.textNom_Marca.setText("");
        this.textNom_Seccion.setText("");
        this.textNom_Procedencia.setText("");
        this.btnNuevo.setEnabled(Agrega_OK);
        this.btnModificar.setEnabled(Modifica_OK);
        this.btnBorrar.setEnabled(Borra_OK);
        this.btnGrabar.setEnabled(false);
        this.btnCancelar.setEnabled(false);
        this.btnSalir.setEnabled(true);
        this.txtCod_Producto.setBuscar(true);
        if (this.btnNuevo.isEnabled()) {
            this.btnNuevo.grabFocus();
        } else if (this.btnModificar.isEnabled()) {
            this.btnModificar.grabFocus();
        } else if (this.btnBorrar.isEnabled()) {
            this.btnBorrar.grabFocus();
        } else {
            this.btnSalir.grabFocus();
        }
    }

    @Override
    public void Agregar() {
        this.jTab.setSelectedIndex(0);
        this.jTab.setEnabledAt(1, false);
        if (this.txtCod_Producto.autoNumerico()) {
            ClaseBotones.modoEdicionABM(btnNuevo, btnModificar, btnBorrar, btnGrabar, btnCancelar, btnSalir, false);
            ClaseCampos.setEnabled(jPanelDatos, true);
            this.txtComentario.setEnabled(true);
            this.txtCosto_Actual.setEnabled(false);
            this.txtPrecioV_Actual.setEnabled(false);
            this.btnGrabar.setEnabled(false);
            this.txtCod_Producto.setBuscar(false);
            this.txtNombre_Producto.setEnabled(true);
            Operacion = 'A';
            this.txtCod_Producto.setEnabled(false);
            this.txtNombre_Producto.grabFocus();
        } else {
            Inicializar();
        }
    }

