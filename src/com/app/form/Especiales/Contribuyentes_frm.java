    private void CargarDatos(String file) {
        miTabla.setRowCount(0);
        try {
            FileReader fileR = new FileReader(new File(file));
            String datos;
            try (BufferedReader bufferR = new BufferedReader(fileR)) {
                while ((datos = bufferR.readLine()) != null) {
                    //datos = datos.replaceAll("|", ";");                    
                   // datos = datos.replaceAll("&#039;", "");                                     
                    datos = datos.replaceAll(";;", ";");                                     
                    datos = datos.replaceAll("'", ".");  
                    //datos = datos.replaceAll("¿¿", "ñ");
                    //datos = datos.replaceAll("¿", "n");
                    miTabla.addRow(datos.split(";"));
                }
                bufferR.close();
                fileR.close();
            } catch (Exception e) {
                MensajeSistema.setException("Error al Crear el Buffer...", e);
            }
        } catch (FileNotFoundException e) {
            MensajeSistema.setException("Se produjo un error al Abrir el archivo", e);
        }
    }

