    private void txtCodigoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtCodigoActionPerformed
        if (this.txtCodigo.verificarVacioSinMsj()) {
            this.txtCodigo.setEnabled(false);
            if (operacion == 'M' || operacion == 'E') {
                this.RecuperarDatos(this.txtCodigo.getText());
            } else {
                this.txtDenominacion.setEnabled(true);
                this.txtDenominacion.grabFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoActionPerformed

    private void txtCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyPressed
        if (evt.getKeyCode() == ClaseTeclas.VK_F5()) {
            if (operacion != 'A') {
                Buscar(tablaConsutada, UsarEmpresa, UsarSucursal, idConsultada, descripcionConsultada, tituloVentanaActual);
                this.txtCodigo.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoKeyPressed

    private void txtDenominacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDenominacionActionPerformed
        this.txtRuc.setEnabled(true);
        this.txtRuc.grabFocus();
    }//GEN-LAST:event_txtDenominacionActionPerformed

    private void txtRucActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRucActionPerformed
        String valor = this.txtRuc.getText().trim();
        if (valor.contains("-") && valor.length() > 0) {
            valor = valor.substring(0, valor.indexOf("-"));
        }
        this.txtRuc.setText(DigitoVerificador.getRUC_DV(valor));
        this.txtRepresentante.setEnabled(true);
        this.txtRepresentante.grabFocus();
    }//GEN-LAST:event_txtRucActionPerformed

    private void txtRepresentanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRepresentanteActionPerformed
        if (this.txtRepresentante.verificarVacioSinMsj()) {
            this.txtRucRepre.setEnabled(true);
            this.txtRucRepre.grabFocus();
        } else {
            this.txtRucRepre.setText("");
            this.txtRucRepre.setEnabled(false);
            this.jCheckAgenteRetentor.setEnabled(true);
            this.jCheckAgenteRetentor.grabFocus();
        }
    }//GEN-LAST:event_txtRepresentanteActionPerformed

    private void txtRucRepreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtRucRepreActionPerformed
        String valor = this.txtRucRepre.getText().trim();
        if (valor.contains("-")) {
            valor = valor.substring(0, valor.indexOf("-"));
        }
        this.txtRucRepre.setText(com.app.clases.utilidades.DigitoVerificador.getRUC_DV(valor));
        this.jCheckAgenteRetentor.setEnabled(true);
        this.jCheckAgenteRetentor.grabFocus();
    }//GEN-LAST:event_txtRucRepreActionPerformed

